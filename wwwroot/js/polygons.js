canvas.on('mouse:wheel', function(opt) {
    var delta = -opt.e.deltaY;
    var zoom = canvas.getZoom();
    zoom = zoom + delta/200;
    if (zoom > 5) zoom = 5;
    if (zoom < 1) zoom = 1;
    canvas.zoomToPoint({ x: opt.e.offsetX, y: opt.e.offsetY }, zoom);
    opt.e.preventDefault();
    opt.e.stopPropagation();
    var vpt = this.viewportTransform;
    if (zoom < 400 / 1000) {
        this.viewportTransform[4] = 200 - 1000 * zoom / 2;
        this.viewportTransform[5] = 200 - 1000 * zoom / 2;
    } else {
        if (vpt[4] >= 0) {
            this.viewportTransform[4] = 0;
        } else if (vpt[4] < canvas.getWidth() - 1000 * zoom) {
            this.viewportTransform[4] = canvas.getWidth() - 1000 * zoom;
        }
        if (vpt[5] >= 0) {
            this.viewportTransform[5] = 0;
        } else if (vpt[5] < canvas.getHeight() - 1000 * zoom) {
            this.viewportTransform[5] = canvas.getHeight() - 1000 * zoom;
        }
    }})

canvas.on('mouse:down', function(opt) {
    var evt = opt.e;
    if (opt.button == 3) {
        this.isDragging = true;
        this.selection = false;
        this.lastPosX = evt.clientX;
        this.lastPosY = evt.clientY;
    }
});
canvas.on('mouse:move', function(opt) {
    if (this.isDragging) {
        var e = opt.e;
        this.viewportTransform[4] += e.clientX - this.lastPosX;
        this.viewportTransform[5] += e.clientY - this.lastPosY;
        this.requestRenderAll();
        this.lastPosX = e.clientX;
        this.lastPosY = e.clientY;
    }
});
canvas.on('mouse:up', function(opt) {
    this.isDragging = false;
    this.selection = true;
});


    
canvas.on('mouse:over', function(e) {
    e.target.set('fill', '#379c37');
    canvas.renderAll();
});

canvas.on('mouse:out', function(e) {
    e.target.set('fill', '#0c4900');
    canvas.renderAll();
});

